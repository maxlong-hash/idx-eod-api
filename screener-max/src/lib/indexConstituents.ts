const INDEX_MEMBER_SETS = new Map<IndexFilterMode, Set<string>>();

export function getIndexFilterMeta(mode: IndexFilterMode) {
  return INDEX_FILTER_OPTIONS.find((option) => option.value === mode) ?? INDEX_FILTER_OPTIONS[0];
}

export function getIndexConstituentCount(mode: IndexFilterMode) {
  if (mode === 'all') return null;
  return INDEX_CONSTITUENTS[mode].length;
}

export function getIndexMemberSet(mode: IndexFilterMode) {
  if (mode === 'all') return null;
  const cached = INDEX_MEMBER_SETS.get(mode);
  if (cached) return cached;
  const next = new Set<string>(INDEX_CONSTITUENTS[mode]);
  INDEX_MEMBER_SETS.set(mode, next);
  return next;
}
